                if (rbtn_male.isSelected()) {
                    employee_gender = "Male";
                } else {
                    employee_gender = "Femal";
                }

                String employee_dept = String.valueOf(emp_dept.getSelectedItem());
                PreparedStatement stmt = con.prepareStatement("update employees set emp_name=?,emp_salary=?,emp_gender=?,dept_id=? where emp_id=?");
                stmt.setString(1, employee_name);
