    private void fillTableModel() {

        try {
            dtm.setRowCount(0);
            PreparedStatement stmt = con.prepareStatement("select car_name,carcolor,carmodel,carcost from cartype");
            ResultSet res = stmt.executeQuery();
            while (res.next()) {
                
                Object[] data = new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4)};
                dtm.addRow(data);
            }
            show_data.setModel(dtm);

        } catch (SQLException ex) {
            Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

