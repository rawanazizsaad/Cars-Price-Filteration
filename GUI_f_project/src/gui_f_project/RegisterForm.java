    private void emp_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emp_registerActionPerformed
        // TODO add your handling code here:

        if (!emp_name.getText().isEmpty() && emp_salary.getText().length() > 0) {
            try {
                String employee_name = emp_name.getText();
                int employee_salary = Integer.parseInt(emp_salary.getText());
                String employee_gender;
                if (rbtn_male.isSelected()) {
                    employee_gender = "Male";
                } else {
                    employee_gender = "Female";
                }

                String employee_dept = String.valueOf(emp_dept.getSelectedItem());
             
                PreparedStatement stmt = con.prepareStatement("insert into employees(emp_name,emp_salary,emp_gender,dept_id) values(?,?,?,?)");
                stmt.setString(1, employee_name);
                stmt.setInt(2, employee_salary);
                stmt.setString(3, employee_gender);
                stmt.setInt(4, dept_ids.get(emp_dept.getSelectedIndex()));
                int res = stmt.executeUpdate();
                if (res > 0) {
                    JOptionPane.showMessageDialog(this, "Successed insert");
                    fillTableModel();
                } else {
                    JOptionPane.showMessageDialog(this, "Faild insert");
                }
////
            } catch (SQLException ex) {
                Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_emp_registerActionPerformed

